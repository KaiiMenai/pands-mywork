# The short if statement is a one-liner that allows you to execute a statement if a condition is true.
# It is used when you have only one statement to execute.

a = 200
b = 33
if a > b: print("a is greater than b")

# Output:
    # a is greater than b
    # This will print "a is greater than b" as 200 is greater than 33.

